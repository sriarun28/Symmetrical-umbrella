class Datatypes
{
	public static void main (String[]args)
	{
		byte a=127;
		short b=23501;
		int c=234347847;
		long d=9834589876l;
		float e=1.3456f;
		double f=4.12345678;
		char g='c';
		boolean h=true;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}
} 